import { mkdir, readFile, readdir, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const pkgDir = path.join(path.dirname(fileURLToPath(import.meta.url)), '..')
const repoRoot = path.join(pkgDir, '..', '..')
const upstreamDir = path.join(repoRoot, 'assets', 'upstream', 'textmate-grammars-themes')
const distDir = path.join(pkgDir, 'dist')
const chunksDir = path.join(distDir, 'chunks')
const outFile = path.join(distDir, 'standard-catalog.mjs')

const LANGUAGE_EXPORT_OVERRIDES = new Map([
  ['angular-html', 'f'],
])

async function main() {
  const [languagesRaw, themesRaw, chunkFiles] = await Promise.all([
    readJson(path.join(upstreamDir, 'languages.json')),
    readJson(path.join(upstreamDir, 'themes.json')),
    readdir(chunksDir),
  ])

  const availableChunks = new Set(
    chunkFiles
      .filter(name => name.endsWith('.mjs'))
      .map(name => path.basename(name, '.mjs')),
  )

  const languages = languagesRaw.languages
    .filter(entry => availableChunks.has(entry.id))
    .map(entry => ({
      id: entry.id,
      name: entry.display_name ?? humanizeId(entry.id),
      aliases: Array.isArray(entry.aliases) ? entry.aliases : [],
      importCode: languageImportCode(entry.id),
    }))
    .sort((a, b) => a.id.localeCompare(b.id))

  const themes = themesRaw.themes
    .filter(entry => availableChunks.has(entry.id))
    .map(entry => ({
      id: entry.id,
      displayName: normalizeThemeDisplayName(entry.display_name, entry.id),
      type: entry.theme_type ?? null,
      importCode: simpleImportCode(entry.id),
    }))
    .sort((a, b) => a.id.localeCompare(b.id))

  await mkdir(distDir, { recursive: true })
  await writeFile(outFile, renderModule({ languages, themes }), 'utf8')

  console.log(`[ferriki] Generated standard catalog: ${path.relative(repoRoot, outFile)}`)
  console.log(`[ferriki] Languages: ${languages.length}, themes: ${themes.length}`)
}

function languageImportCode(id) {
  const exportName = LANGUAGE_EXPORT_OVERRIDES.get(id)
  if (!exportName) {
    return simpleImportCode(id)
  }
  return `() => import('./chunks/${id}.mjs').then(function (n) { return n.${exportName}; })`
}

function simpleImportCode(id) {
  return `() => import('./chunks/${id}.mjs')`
}

function normalizeThemeDisplayName(displayName, id) {
  if (typeof displayName === 'string' && displayName.trim().length > 0) {
    return looksMachineLowercase(displayName) ? humanizeId(id) : displayName
  }
  return humanizeId(id)
}

function looksMachineLowercase(value) {
  return value === value.toLowerCase() && !/[A-Z]/.test(value)
}

function humanizeId(id) {
  return id
    .split('-')
    .map(segment => segment.length <= 3 ? segment.toUpperCase() : `${segment[0].toUpperCase()}${segment.slice(1)}`)
    .join(' ')
}

async function readJson(file) {
  return JSON.parse(await readFile(file, 'utf8'))
}

function renderModule({ languages, themes }) {
  const languageRows = languages.map((entry) => {
    const aliases = entry.aliases.length
      ? `,\n    aliases: ${JSON.stringify(entry.aliases)}`
      : ''
    return `  {\n    id: ${JSON.stringify(entry.id)},\n    name: ${JSON.stringify(entry.name)}${aliases},\n    import: ${entry.importCode}\n  }`
  }).join(',\n')

  const themeRows = themes.map(entry => `  {\n    id: ${JSON.stringify(entry.id)},\n    displayName: ${JSON.stringify(entry.displayName)},\n    type: ${JSON.stringify(entry.type)},\n    import: ${entry.importCode}\n  }`).join(',\n')

  return `// Generated by node/ferriki/scripts/generate-standard-catalog.mjs\nexport const bundledLanguagesInfo = [\n${languageRows}\n]\n\nexport const bundledLanguagesBase = Object.fromEntries(bundledLanguagesInfo.map(i => [i.id, i.import]))\nexport const bundledLanguagesAlias = Object.fromEntries(bundledLanguagesInfo.flatMap(i => i.aliases?.map(a => [a, i.import]) || []))\nexport const bundledLanguages = {\n  ...bundledLanguagesBase,\n  ...bundledLanguagesAlias,\n}\n\nexport const bundledThemesInfo = [\n${themeRows}\n]\n\nexport const bundledThemes = Object.fromEntries(bundledThemesInfo.map(i => [i.id, i.import]))\n`
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error))
  process.exit(1)
})
